async function main() {
  console.log('🔄 Начинаем массовый импорт рецептов в базу данных...')

  // Очищаем таблицы перед сидом, чтобы избежать дублирования при повторных запусках.
  // За счет onDelete: Cascade в схеме, удаление Recipe автоматически очистит Ingredient.
  await prisma.recipe.deleteMany()

  for (const recipe of recipesData) {
    const createdRecipe = await prisma.recipe.create({
      data: {
        title: recipe.title,
        description: recipe.description,
        cookingTime: recipe.cookingTime,
        servings: recipe.servings,
        difficulty: recipe.difficulty,
        imageUrl: recipe.imageUrl,
        steps: recipe.steps,
        
        // Магия nested-write: Prisma сама свяжет созданные ингредиенты с этим Recipe
        ingredients: {
          create: recipe.ingredients.map(ing => ({
            name: ing.name,
            quantity: ing.quantity,
            unit: ing.unit
          }))
        }
      }
    })
    console.log(`✅ Импортирован рецепт: "${createdRecipe.title}" (ID: ${createdRecipe.id})`)
  }

  console.log('🎉 Импорт успешно завершен!')
}

main()
  .then(async () => {
    await prisma.$disconnect()
  })
  .catch(async (e) => {
    console.error('❌ Ошибка при импорте сидов:', e)
    await prisma.$disconnect()
    process.exit(1)
  })